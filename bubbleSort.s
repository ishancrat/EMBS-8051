ORG 0040H
	JMP START

SWAPPER:MOV A,@R0
		XCH A,B
		DEC R0
		MOV A,@R0
		XCH A,B
		MOV @R0,A
		XCH A,B
		INC R0
		MOV @R0,A
		RET
	
START: MOV R3,#09H
	   MOV R0,#60H

OUTERLOOP:MOV R2,#09H
		  MOV R0,#60H
		  
INNERLOOP:MOV A,@R0
		  XCH A,B
		  INC R0
		  MOV A,@R0
		  SUBB A,B
		  JNC LABEL1
		  ACALL SWAPPER
		  
LABEL1:DJNZ R2,INNERLOOP
	   DJNZ R3,OUTERLOOP
	   
END
