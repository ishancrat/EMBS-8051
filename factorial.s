ORG 0000H
	JMP START

CARRY: INC B
	   JMP CARRY_RETURN

ZERO_FACTORIAL: MOV @R0,#1 ;IF 0! IS TO BE CALCULATED
				
				
START: MOV R0,#6FH
	   MOV @R0,#24 ;FACTORIAL OF NUM TO BE CALCULATED
	   MOV A,@R0
	   JZ ZERO_FACTORIAL
	   DEC A
	   MOV R2,A ;R2 STORES 'NUM-1'
	   MOV R5,#00H
	   

	   
LOOP:  MOV B,R2
	   MOV A,@R0 ;LOWEST BYTE MOVED TO ACCUMULATOR
	   MUL AB
	   ADD A,R5 
	   JC CARRY ;IF ADDING R5 TO 'A' CHANGES THE MSB OF 'A', THEN THE MSB CHANGE IS IMPLEMENTED IN 'CARRY' SUBROUTINE
CARRY_RETURN: MOV @R0,A ;LOWER BYTE
			  MOV R5,B ;TO-BE-ADDED NUMBER STORED IN R5
			  DEC R0
			  CJNE R0,#20H,LOOP
		      MOV R0,#6FH
			  DJNZ R2,LOOP
ENDING:
END   
